    <p>Cara Belanja untuk lebih mepermudah transaksi belanja Anda pada buku.com sebaiknya terlebih dahulu Anda perhatikan langkah-langkah proses belanja dibawah ini:</p>
	<br>
	1. Daftar Menjadi Anggota : <br>
	Langkah ini dilakukan bagi Anda yang belum menjadi Anggota.
	Lakukan "Daftar" yang tersedia di menu kanan atas.
	<br>
	2. Login Anggota : <br>
	Langkah ini dilakukan untuk pembelian buku.<br>
	Lakukan "Login" yang tersedia di menu kanan atas.<br>
	3. Pilih/Cari Buku<br>
	Mencari buku yang ingin dibeli bisa dilakukan dengan melihat kategori-kategori yang ada.<br>
	4. Klik/Tekan Tombol "Lihat Selengkapnya<br>
	Jika sudah mendapatkan buku yang ingin dibeli maka klik tombol "Beli".<br>
	5. Lihat Daftar Keranjang Belanja<br>
	Buku yang telah dipilih (setelah klik tombol "Beli") akan masuk ke dalam daftar "Keranjang Belanja".<br>
	6. Anda bisa mengubah isian jumlah buku pada field/isian "Jumlah" pada tiap judul buku yang ada di daftar "Keranjang Belanja", kemudian klik tombol "  ubah " untuk mendapatkan total harga sesuai jumlah .<br>
	7. Anda bisa membatalkan/menghapus buku yang ada dalam daftar "Keranjang Belanja" jika Anda tidak ingin jadi membeli buku tersebut dengan cara klik tombol "  hapus item ".<br>
	8. Jika ingin mencari/membeli buku-buku lain maka lakukan dari langkah ke-3 lagi.<br>
	9. Klik/Tekan Tombol "Lanjutkan"<br>
	Jika sudah yakin dengan daftar buku yang ingin dibeli pada daftar "Keranjang Belanja" Anda maka Anda tinggal klik tombol "Lanjutkan Konfirmasi Pemesanan" untuk mengisi alamat pengiriman dan cara pembayaran.<br>
	10. Isi Alamat Pengiriman dan Cara Pembayaran<br>
	11. Isi data Anda dengan lengkap seperti Nama, Alamat Pengiriman, dan lain-lain.<br>
	12. Pilih cara pembayaran, cara Anda membayar buku yang Anda beli.<br>
	13. Klik tombol "  Kirim Konfirmasi " untuk melanjutkan ke konfirmasi akhir pesanan belanja anda.<br>
	14. Konfirmasi Pesanan <br>
	Pada langkah ini, Anda akan diminta untuk memeriksa isian sekali lagi tentang daftar buku yang ingin dibeli, total biaya dan informasi pengiriman.<br>
	15. Jika sudah yakin dan benar maka Anda tinggal klik tombol " Selesai " untuk menyelesaikan proses pemesanan anda.<br>
	16. Bayar Pesanan<br>
	Bisa dibaca di Cara Pembayaran<br>
	Untuk keterangan lebih lengkap mengenai cara belanja, silakan tanyakan kepada bagian Admin melalui Hubungi Kami .</p>
